/* 코드를 반복 -> 자동화. 계속해서 반복되는 코드를 직접 일일이 복사하지 않고도 쓸수있는 방법.
 * 루프 : 어떤 기능을 반복하는 것.
 * 같은 일이 연속으로 열번 반복되길 원할 때 코드를 열번 쓰는 대신 루프를 활용해서 반복 가능.
 * 실제로 자동화 되는 것은 아님. 
 * 배열에 있는 모든 숫자의 합계 가능. 
 */

/* for 반복문 문법 
for (
   [initialExpression];
   [condition];
   [incrementExpression]
)
*/

/* 1 ~ 10 출력하기 
i로 시작;
10에서 멈춤;
1씩 증가
for (let i = 1; i <= 10; i++) {
   console.log(i);
}
*/

