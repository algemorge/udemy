// 콘솔창에서는 n번 명령어라고 표기됨.
// i       :  반복문의 목적을 위해 존재하는 것이고 의미없음. i를 1로 설정해서 1부터 시작하게 함.
// i 외에 num 이나 g 같은 다른 말로 넣어도 되나 일관성을 유지해야 한다.
// 중간 부분 : 불리언 표현식. 이 부분이 참으로 평가되면 루프는 계속해서 실행된다.
// 1부터 시작 -> 1이 10보다 작거나 같나요? -> true -> 1 출력
// 2가 10보다 작거나 같나요? -> true -> 2 출력
// 3이 10보다 작거나 같나요? -> true -> 3 출력 
// 4가 10보다 작거나 같나요? -> true -> 4 출력 ... 10출력
// 11이 10보다 작거나 같나요? -> false -> 종료
// 세번째 부분 : 증감 표현식. i 값을 업데이트 

for (let i = 1; i <= 10; i++) {
   console.log("반복문에 갇혀버림");
   console.log(i);
}
